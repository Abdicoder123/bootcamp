#The while loop
i = 1
while i < 21
    if i % 2 == 0
        puts "#{i} is an even number"
    else
        puts "#{i} is an odd number"
    end
    i += 1
end

#The for loop

for x in 1..20
    if x % 2 == 0
        puts "#{x} is an even number"
    else
        puts "#{x} is an odd number"
    end 
end
